/**
 * Apply a team's colors as CSS custom properties on :root.
 * Call on team selection or game load.
 */
export function applyTeamColors(teamName) {
  const colors = TEAM_COLORS[teamName];
  if (!colors) return;

  const root = document.documentElement;
  const p = colors.primary;
  const s = colors.secondary;

  root.style.setProperty('--color-accent', p);
  root.style.setProperty('--color-accent-hover', darken(p, 0.12));
  root.style.setProperty('--color-accent-bg', toRgba(p, 0.07));
  root.style.setProperty('--color-accent-border', toRgba(p, 0.15));
  root.style.setProperty('--color-accent-secondary', s);

  // Ensure text-on-white readability (WCAG AA = 4.5:1)
  root.style.setProperty('--color-accent-text', ensureContrast(p, '#FFFFFF', 4.5));
}

// ── Color utilities ──

function hexToRgb(hex) {
  hex = hex.replace('#', '');
  return [
    parseInt(hex.substring(0, 2), 16),
    parseInt(hex.substring(2, 4), 16),
    parseInt(hex.substring(4, 6), 16),
  ];
}

function toRgba(hex, a) {
  const [r, g, b] = hexToRgb(hex);
  return `rgba(${r}, ${g}, ${b}, ${a})`;
}

function darken(hex, amount) {
  const [r, g, b] = hexToRgb(hex);
  const d = (v) => Math.max(0, Math.round(v * (1 - amount)));
  return '#' + [d(r), d(g), d(b)].map(v => v.toString(16).padStart(2, '0')).join('');
}

function luminance(hex) {
  const [r, g, b] = hexToRgb(hex).map(c => {
    c = c / 255;
    return c <= 0.03928 ? c / 12.92 : Math.pow((c + 0.055) / 1.055, 2.4);
  });
  return 0.2126 * r + 0.7152 * g + 0.0722 * b;
}

function contrastRatio(fg, bg) {
  const l1 = luminance(fg), l2 = luminance(bg);
  return (Math.max(l1, l2) + 0.05) / (Math.min(l1, l2) + 0.05);
}

function ensureContrast(fg, bg, min) {
  let hex = fg;
  for (let i = 0; i < 10; i++) {
    if (contrastRatio(hex, bg) >= min) return hex;
    hex = darken(hex, 0.15);
  }
  return hex;
}
